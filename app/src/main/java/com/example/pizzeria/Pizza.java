package com.example.pizzeria;

public class Pizza {

    double pizza_size_price;
    double peperoni_precio=0;
    double queso_precio=0;
    double pollo_precio=0;
    double carne_precio=0;
    double hongos_precio=0;
    double piña_precio=0;
    double jamon_precio=0;

    public Pizza() {
    }

    public double getPizza_size_price() {
        return pizza_size_price;
    }

    public void setPizza_size_price(double pizza_size_price) {
        this.pizza_size_price = pizza_size_price;
    }

    public double getPeperoni_precio() {
        return peperoni_precio;
    }

    public void setPeperoni_precio(double peperoni_precio) {
        this.peperoni_precio = peperoni_precio;
    }

    public double getQueso_precio() {
        return queso_precio;
    }

    public void setQueso_precio(double queso_precio) {
        this.queso_precio = queso_precio;
    }

    public double getPollo_precio() {
        return pollo_precio;
    }

    public void setPollo_precio(double pollo_precio) {
        this.pollo_precio = pollo_precio;
    }

    public double getCarne_precio() {
        return carne_precio;
    }

    public void setCarne_precio(double carne_precio) {
        this.carne_precio = carne_precio;
    }

    public double getHongos_precio() {
        return hongos_precio;
    }

    public void setHongos_precio(double hongos_precio) {
        this.hongos_precio = hongos_precio;
    }

    public double getPiña_precio() {
        return piña_precio;
    }

    public void setPiña_precio(double piña_precio) {
        this.piña_precio = piña_precio;
    }

    public double getJamon_precio() {
        return jamon_precio;
    }

    public void setJamon_precio(double jamon_precio) {
        this.jamon_precio = jamon_precio;
    }
}
